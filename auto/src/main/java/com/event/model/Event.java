package com.event.model;

public class Event {
	
	
	private String position;
	private String rating;
	private String reviews_original;
	private String reviews;
	private String description;
	private String lsig;
	private String thumbnail; 
	private String title;
	private String type;
	private String hours;
	private String address;
	private String place_id;
	private String place_id_search;
	private String  latitude;
	private String  longitude;
	
	
	public String getHours() {
		return hours;
	}
	public void setHours(String hours) {
		this.hours = hours;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}
	public String getReviews_original() {
		return reviews_original;
	}
	public void setReviews_original(String reviews_original) {
		this.reviews_original = reviews_original;
	}
	public String getReviews() {
		return reviews;
	}
	public void setReviews(String reviews) {
		this.reviews = reviews;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getLsig() {
		return lsig;
	}
	public void setLsig(String lsig) {
		this.lsig = lsig;
	}
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPlace_id() {
		return place_id;
	}
	public void setPlace_id(String place_id) {
		this.place_id = place_id;
	}
	public String getPlace_id_search() {
		return place_id_search;
	}
	public void setPlace_id_search(String place_id_search) {
		this.place_id_search = place_id_search;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	
}


