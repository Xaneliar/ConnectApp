package com.connect.events.APIHandler;

public class Event {
	
	
	private String name;
	private String link;
	private String serpapi_link;
	private String image;
	private String description;
	private String lsig;
	private String thumbnail; 
	private String title;
	private String type;
	private String hours;
	private String address;
	private String place_id;
	private String place_id_search;
	private String  latitude;
	private String  longitude;
	
	
	public String getHours() {
		return hours;
	}
	public void setHours(String hours) {
		this.hours = hours;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getSerpapi_link() {
		return serpapi_link;
	}
	public void setSerpapi_link(String serpapi_link) {
		this.serpapi_link = serpapi_link;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getLsig() {
		return lsig;
	}
	public void setLsig(String lsig) {
		this.lsig = lsig;
	}
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPlace_id() {
		return place_id;
	}
	public void setPlace_id(String place_id) {
		this.place_id = place_id;
	}
	public String getPlace_id_search() {
		return place_id_search;
	}
	public void setPlace_id_search(String place_id_search) {
		this.place_id_search = place_id_search;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	
}